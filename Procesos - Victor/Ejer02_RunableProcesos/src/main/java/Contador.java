
public class Contador {

	private int Contador;

	public Contador(int contador) {
		super();
		Contador = contador;
	}

	public int getContador() {
		return Contador;
	}

	public void setContador(int contador) {
		Contador = contador;
	}

	@Override
	public String toString() {
		return "Contador [Contador=" + Contador + "]";
	}
	
	
	
}
